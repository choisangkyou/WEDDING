package modal;

public class Category {
	private String Code;
	private String Descript;
	private int Use;
	
	
	public String getCode() {
		return Code;
	}
	public void setCode(String code) {
		Code = code;
	}
	public String getDescript() {
		return Descript;
	}
	public void setDescript(String descript) {
		Descript = descript;
	}
	public int getUse() {
		return Use;
	}
	public void setUse(int use) {
		Use = use;
	}
	
	
}
